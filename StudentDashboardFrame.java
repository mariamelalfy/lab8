    private void openBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openBtnActionPerformed
       String selected = enrolledList.getSelectedValue();
    
    // Check if selected
    if (selected == null) {
        JOptionPane.showMessageDialog(this, "Please select a course!");
        return;
    }
    
    try {
        // Get course ID
        String[] parts = selected.split(" - ");
        int courseId = Integer.parseInt(parts[0]);
        String courseName = parts[1];
        
        // Check for certificate
        JsonDatabaseManager db = new JsonDatabaseManager();
        CertificateService cs = new CertificateService(db);
        
        // Check if completed
        if (cs.isEligibleForCertificate(student.getUserId(), String.valueOf(courseId))) {
            // Check if already has certificate
            Certificate exist = cs.getCertificateForCourse(student.getUserId(), String.valueOf(courseId));
            
            if (exist == null) {
                // Make new certificate
                Certificate newCert = cs.generateCertificate(student.getUserId(), String.valueOf(courseId));
                
                if (newCert != null) {
                    JOptionPane.showMessageDialog(this, 
                        "Congratulations! You earned a certificate!\nCheck 'My Certificates' button.");
                }
            }
        }
        
        // Open course details
        JFrame parent = (JFrame) SwingUtilities.getWindowAncestor(this);
        CourseDetailsDialog dialog = new CourseDetailsDialog(parent, student, service, courseId, courseName);
        dialog.setVisible(true);
        
        // Refresh
        loadEnrolledCourses();
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_openBtnActionPerformed

