    private void enrollBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enrollBtnActionPerformed
String selected = availableList.getSelectedValue();
        if (selected == null) {
            JOptionPane.showMessageDialog(this, 
                "Please select a course from 'Available Courses' first!", 
                "No Selection", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            int courseId = Integer.parseInt(selected.split(" - ")[0]);
            
            boolean ok = service.enrollInCourse(student.getUserId(), courseId);
            
            if (ok) {
                JOptionPane.showMessageDialog(this, 
                    "Successfully enrolled in the course!", 
                    "Enrollment Success", 
                    JOptionPane.INFORMATION_MESSAGE);
                loadEnrolledCourses();
                loadAvailableCourses();
            } else {
                JOptionPane.showMessageDialog(this, 
                    "You are already enrolled in this course!", 
                    "Already Enrolled", 
                    JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "An error occurred during enrollment: " + e.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_enrollBtnActionPerformed

    private void certificatesBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_certificatesBtnActionPerformed
    JsonDatabaseManager db = new JsonDatabaseManager();
    CertificateService cs = new CertificateService(db);
    
    // Get certificates
    List<Certificate> myCerts = cs.getStudentCertificates(student.getUserId());
    
    // Make a dialog
    JDialog dialog = new JDialog();
    dialog.setTitle("My Certificates");
    dialog.setSize(650, 450);
    dialog.setLocationRelativeTo(this);
    dialog.setModal(true);
    
    // Make main panel
    JPanel panel = new JPanel();
    panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS));
    panel.setBackground(Color.WHITE);
    
    // Add title
    JLabel title = new JLabel("My Certificates");
    title.setFont(new Font("Arial", Font.BOLD, 20));
    panel.add(title);
    panel.add(Box.createVerticalStrut(10));
    
    // Check if empty
    if (myCerts.size() == 0) {
        JLabel noLabel = new JLabel("No certificates yet!");
        noLabel.setFont(new Font("Arial", Font.PLAIN, 14));
        panel.add(noLabel);
    } else {
        // Show each certificate
        for (int i = 0; i < myCerts.size(); i++) {
            Certificate c = myCerts.get(i);
            
            // Make a panel for this certificate
            JPanel certPanel = new JPanel();
            certPanel.setLayout(new FlowLayout(FlowLayout.LEFT));
            certPanel.setBackground(new Color(240, 240, 240));
            certPanel.setBorder(BorderFactory.createLineBorder(Color.GRAY));
            
            // Add icon
            JLabel icon = new JLabel("Certificate");
            icon.setFont(new Font("Arial", Font.BOLD, 14));
            certPanel.add(icon);
            
            // Add course name
            JLabel courseName = new JLabel(c.getCourseTitle());
            courseName.setFont(new Font("Arial", Font.PLAIN, 12));
            certPanel.add(courseName);
            
            // Add score
            JLabel score = new JLabel("Score: " + c.getFinalScore() + "%");
            score.setFont(new Font("Arial", Font.PLAIN, 11));
            certPanel.add(score);
            
            // Add view button
            JButton viewBtn = new JButton("View");
            final Certificate cert = c; // for the button click
            viewBtn.addActionListener(new ActionListener() {
                public void actionPerformed(ActionEvent e) {
                    showCertificate(cert);
                }
            });
            certPanel.add(viewBtn);
            
            panel.add(certPanel);
            panel.add(Box.createVerticalStrut(5));
        }
    }
    
    // Add to scroll pane
    JScrollPane scroll = new JScrollPane(panel);
    dialog.add(scroll);
    
    // Show dialog
    dialog.setVisible(true);
    }//GEN-LAST:event_certificatesBtnActionPerformed

