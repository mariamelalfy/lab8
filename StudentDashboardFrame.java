    private void loadAvailableCourses() {
        DefaultListModel<String> model = new DefaultListModel<>();
        try {
            for (Course c : service.browseAvailableCourses()) {
                model.addElement(c.getCourseID() + " - " + c.getCourseTitle());
            }
            availableList.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error loading available courses: " + e.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
             model.addElement("Error loading courses");
            availableList.setModel(model);
        }
    }

private void loadEnrolledCourses() {
        DefaultListModel<String> model = new DefaultListModel<>();
        try {
            List<Course> courses = service.getEnrolledCourses(student.getUserId());
            
            if (courses.isEmpty()) {
                model.addElement("No enrolled courses yet");
            } else {
                for (Course c : courses) {
                    model.addElement(c.getCourseID() + " - " + c.getCourseTitle());
                }
            }
            
            enrolledList.setModel(model);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error loading enrolled courses: " + e.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
            model.addElement("Error loading courses");
            enrolledList.setModel(model);
        }
    }


