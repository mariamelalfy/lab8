      private void populateCertificateData() {
        // Get student name from certificate and display it
        studentNameLabel.setText(certificate.getStudentName());
        
        // Get course title from certificate and display it
        courseTitleLabel.setText(certificate.getCourseTitle());
        
        // Get final score from certificate and display it with formatting
        scoreLabel.setText(String.format("Final Score: %.1f%%", certificate.getFinalScore()));
        
        // Get instructor name from certificate and display it
        instructorLabel.setText("Instructor: " + certificate.getInstructorName());
        
        // Format the date nicely (e.g., "November 24, 2025")
        SimpleDateFormat sdf = new SimpleDateFormat("MMMM dd, yyyy");
        dateLabel.setText("Issue Date: " + sdf.format(certificate.getIssueDate()));
        
        // Format certificate ID with leading zeros (e.g., CERT-0001)
        certIdLabel.setText("Certificate ID: CERT-" + String.format("%04d", certificate.getCertificateID()));
    }
